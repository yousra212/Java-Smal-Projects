package src;
public class ObjectDataTypesExample {
    public static void main(String[] args) {
                Byte bObj = 127;
                Short sObj = 32767;
                Integer iObj = 2147483647;
                Long lObj = 9223372036854775807L;
                Float fObj = 3.4028235E38f;
                Double dObj = 1.7976931348623157E308;
                Character cObj = 'a';
                Boolean boolObj = true;
                Integer nullObj = null;

                System.out.println("Byte: " + bObj);
                System.out.println("Short: " + sObj);
                System.out.println("Int: " + iObj);
                System.out.println("Long: " + lObj);
                System.out.println("Float: " + fObj);
                System.out.println("Double: " + dObj);
                System.out.println("Char: " + cObj);
                System.out.println("Boolean: " + boolObj);
                System.out.println("Null: " + nullObj);
    }
}

