package src;

public class DataTypesPrimitives {
    public static void main(String[] args) {
        //Numeric primitives
        byte myNumb = 16;
        System.out.println(myNumb);
        short myNumb2 = 32760;
        System.out.println(myNumb2);
        int myNumb3 = 1468798;
        System.out.println(myNumb3);
        long myNumb4 = 15000000000L;
        System.out.println(myNumb4);
        float myNum5 = 5.757382f;
        System.out.println(myNum5);
        double myNum6 = 19.9983729273d;
        System.out.println(myNum6);
        //Logical primitives
        boolean isJavaFun = true;
        System.out.println(isJavaFun);
        //Textual primitives
        char myLetter = 'B';
        System.out.println(myLetter);
    }
}
